from typing import Any

import pytest

from src.widget import get_date, mask_account_card


@pytest.mark.parametrize(
    "value, result",
    [
        ("Visa Platinum 7000792289606361", "Visa Platinum 7000 79** **** 6361"),
        ("Счет 73654108430135874305", "Счет **4305"),
        ("Счет 2345", ""),  # Тест: неполный номер карты (меньше 16 цифр)
        ("", ""),
        ("Просто текст", ""),
        ("Карта 1234567890123456 и ещё 9876543210987654", "Карта 1234 56** **** 3456"),  # Несколько 16‑значных
        ("Счет 12345678901234567890 и карта 1111222233334444", "error: invalid format"),  # 20‑ и 16‑значный
        # Тест: одновременное наличие 16- и 20-значного номера
        ("Карта 1234567890123456 и счёт 12345678901234567890", "error: invalid format"),
    ],
)
def test_mask_account_card(value: str, result: str) -> None:
    assert mask_account_card(value) == result


@pytest.mark.parametrize(
    "value, result",
    [
        ("2024-03-11T02:26:18.671407", "11.03.2024"),
        ("2024-03-11", "11.03.2024"),
        ("", "в строке дата формата 'YYYY-MM-DD' не найдена"),
        ("Дата: 2024-03-11 в тексте", "11.03.2024"),  # Дата в тексте
        ("2024.03.11", "в строке дата формата 'YYYY-MM-DD' не найдена"),  # Неверный формат
        ("", "в строке дата формата 'YYYY-MM-DD' не найдена"),  # Пустая строка
        ("Текст без даты", "в строке дата формата 'YYYY-MM-DD' не найдена"),  # Нет даты
        ("2024/03/11", "в строке дата формата 'YYYY-MM-DD' не найдена"),  # Другой разделитель
        ("9999-99-99", "error: invalid format"),  # Некорректная дата
        # Тест: некорректный месяц
        ("2024-13-01", "error: invalid format"),
        # Тест: некорректный день
        ("2024-02-30", "error: invalid format"),  # в феврале нет 30-го
        # Тест: високосный год (крайний случай)
        ("2024-02-29", "29.02.2024"),  # 2024 — високосный
        # Тест: не високосный год
        ("2023-02-29", "error: invalid format"),  # в 2023 нет 29 февраля
        ("2024-03-11 текст после", "11.03.2024"),
        ("2024-03-11T00:00:00", "11.03.2024"),
    ],
)
def test_get_date(value: Any, result: Any) -> None:
    assert get_date(value) == result
